package sdacademy.pl.mypersonslist;

public class Person {

    private String name;
    private int age;

    public Person() {
    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

//    nie jest dobra praktyka modyfikowanie w toStringm aby wyswietlac
//    modyfikacje w MainActivity, jak nam nie odpowiada, albo
//    stworzyc osobna klase do formatowania


    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
